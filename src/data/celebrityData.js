export const getRandomCelebrity = () => {
  const randomIndex = Math.floor(Math.random() * celebrityData.length);
  return celebrityData[randomIndex];
};

export const getRandomCelebrities = (count) => {
  const shuffled = [...celebrityData].sort(() => 0.5 - Math.random());
  return shuffled.slice(0, Math.min(count, celebrityData.length));
};

export const getCelebritiesByCategory = (category) => {
  return celebrityData.filter(celeb => celeb.category === category);
};

export const getCelebritiesByProvince = (province) => {
  return celebrityData.filter(celeb => celeb.birthProvince === province);
};

export const getCelebrityPhoto = (celebrity) => {
  const path = celebrity.photo || '/images/celebrities/placeholder.jpg';
  const cleanPath = path.startsWith('/') ? path.slice(1) : path;
  return `${import.meta.env.BASE_URL}${cleanPath}`;
};

export const getAllProvinces = () => {
  const provinces = [...new Set(celebrityData.map(celeb => celeb.birthProvince))];
  return provinces.sort();
};

export default celebrityData;

