export const getProvinceByName = (name) => {
  return turkeyProvinces.find(p => p.name.toLowerCase() === name.toLowerCase());
};

export const getProvinceByCode = (code) => {
  return turkeyProvinces.find(p => p.code === code);
};

export const getProvincesByRegion = (region) => {
  return turkeyProvinces.filter(p => p.region === region);
};

export const getAllRegions = () => {
  return [...new Set(turkeyProvinces.map(p => p.region))].sort();
};

export const getRandomProvinces = (count) => {
  const shuffled = [...turkeyProvinces].sort(() => 0.5 - Math.random());
  return shuffled.slice(0, Math.min(count, turkeyProvinces.length));
};

export const getNearbyProvinces = (provinceName, count = 3) => {
  const targetProvince = getProvinceByName(provinceName);
  if (!targetProvince) return [];

  const distances = turkeyProvinces
    .filter(p => p.name !== provinceName)
    .map(p => ({
      province: p,
      distance: Math.sqrt(
        Math.pow(p.coordinates[0] - targetProvince.coordinates[0], 2) +
        Math.pow(p.coordinates[1] - targetProvince.coordinates[1], 2)
      )
    }))
    .sort((a, b) => a.distance - b.distance);

  return distances.slice(0, count).map(d => d.province);
};

